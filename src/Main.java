public class Main {
    public static void main(String[] args) {
        // Primitive data type
        int x = 10;
        double y = 20.5;

        // Reference data type (String is a class in Java)
        String greeting = "Hello, Java!";

        // Reference data type (Array)
        int[] numbers = {1, 2, 3, 4, 5};

        // Printing values
        System.out.println("Primitive int: " + x);
        System.out.println("Primitive double: " + y);
        System.out.println("Reference String: " + greeting);
        System.out.println("Reference Array: " + numbers[0]);
    }
}
